CREATE PROCEDURE SP_ThongTinPhong
    @MAPHONG VARCHAR(10)
AS
BEGIN
    SELECT P.TENPHONG, LP.TENLP, T.TENTANG, LP.DONGIA
    FROM PHONG P
        INNER JOIN LOAIPHONG LP ON P.MALP = LP.MALP
        INNER JOIN TANG T ON P.MATANG = T.MATANG
    WHERE P.MAPHONG = @MAPHONG;
END

