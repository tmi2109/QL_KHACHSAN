CREATE TABLE NHANVIEN
(
	MANV NVARCHAR(10) NOT NULL,
	TENNV NVARCHAR(50),
	GIOITINH NCHAR(5),
	NGAYSINH DATE,
	DIACHI NVARCHAR(100),
	SDT NVARCHAR(10),
